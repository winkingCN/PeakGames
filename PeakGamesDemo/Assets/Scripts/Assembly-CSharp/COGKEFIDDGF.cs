using System;
using Org.BouncyCastle.Math;

public class COGKEFIDDGF : IGMAJHKIIPK
{
	private readonly PLGGNEJEIIP ECPKCNOAOAJ;

	private readonly BigInteger IMDGDOACCOG;

	public PLGGNEJEIIP OIDHEOJEMJF
	{
		get
		{
			return ECPKCNOAOAJ;
		}
	}

	public BigInteger CCFCHIEOCPE
	{
		get
		{
			return IMDGDOACCOG;
		}
	}

	public COGKEFIDDGF(PLGGNEJEIIP ECPKCNOAOAJ, BigInteger IMDGDOACCOG)
	{
		if (ECPKCNOAOAJ.CCLHEKOIIKM)
		{
			throw new ArgumentException("RSA parameters should be for a public key");
		}
		this.ECPKCNOAOAJ = ECPKCNOAOAJ;
		this.IMDGDOACCOG = IMDGDOACCOG;
	}
}
