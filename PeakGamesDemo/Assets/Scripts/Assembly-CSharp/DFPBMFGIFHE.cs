using Utils.Game;

public class DFPBMFGIFHE
{
	private readonly string BPAOHKDBCID;

	private readonly GamePlayRecord EBHFCNDCJKE;

	public DFPBMFGIFHE(string DBONNDOKLCF, GamePlayRecord HPFPDJHBNIE)
	{
		BPAOHKDBCID = DBONNDOKLCF;
		EBHFCNDCJKE = HPFPDJHBNIE;
	}

	public override string ToString()
	{
		return BPAOHKDBCID + EBHFCNDCJKE;
	}
}
