using System;

namespace Org.BouncyCastle.Crypto
{
	[Serializable]
	public class InvalidCipherTextException : CryptoException
	{
		public InvalidCipherTextException()
		{
		}

		public InvalidCipherTextException(string OEFCOJLNCKP)
			: base(OEFCOJLNCKP)
		{
		}

		public InvalidCipherTextException(string OEFCOJLNCKP, Exception FGDHECKFFDP)
			: base(OEFCOJLNCKP, FGDHECKFFDP)
		{
		}
	}
}
