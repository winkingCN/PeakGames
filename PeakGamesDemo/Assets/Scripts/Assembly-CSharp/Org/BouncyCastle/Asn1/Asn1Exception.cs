using System;
using System.IO;

namespace Org.BouncyCastle.Asn1
{
	[Serializable]
	public class Asn1Exception : IOException
	{
		public Asn1Exception()
		{
		}

		public Asn1Exception(string OEFCOJLNCKP)
			: base(OEFCOJLNCKP)
		{
		}

		public Asn1Exception(string OEFCOJLNCKP, Exception FGDHECKFFDP)
			: base(OEFCOJLNCKP, FGDHECKFFDP)
		{
		}
	}
}
