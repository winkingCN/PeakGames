using System;

namespace Org.BouncyCastle.Security.Certificates
{
	[Serializable]
	public class CertificateNotYetValidException : CertificateException
	{
		public CertificateNotYetValidException()
		{
		}

		public CertificateNotYetValidException(string OEFCOJLNCKP)
			: base(OEFCOJLNCKP)
		{
		}

		public CertificateNotYetValidException(string OEFCOJLNCKP, Exception FGDHECKFFDP)
			: base(OEFCOJLNCKP, FGDHECKFFDP)
		{
		}
	}
}
