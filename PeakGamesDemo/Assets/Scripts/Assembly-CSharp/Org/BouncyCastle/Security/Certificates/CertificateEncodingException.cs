using System;

namespace Org.BouncyCastle.Security.Certificates
{
	[Serializable]
	public class CertificateEncodingException : CertificateException
	{
		public CertificateEncodingException()
		{
		}

		public CertificateEncodingException(string NOIOHIPHFHA)
			: base(NOIOHIPHFHA)
		{
		}

		public CertificateEncodingException(string NOIOHIPHFHA, Exception EKFOKNPODBK)
			: base(NOIOHIPHFHA, EKFOKNPODBK)
		{
		}
	}
}
