using System;

namespace Org.BouncyCastle.Security.Certificates
{
	[Serializable]
	public class CertificateExpiredException : CertificateException
	{
		public CertificateExpiredException()
		{
		}

		public CertificateExpiredException(string OEFCOJLNCKP)
			: base(OEFCOJLNCKP)
		{
		}

		public CertificateExpiredException(string OEFCOJLNCKP, Exception FGDHECKFFDP)
			: base(OEFCOJLNCKP, FGDHECKFFDP)
		{
		}
	}
}
